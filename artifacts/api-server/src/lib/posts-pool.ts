export function getPostForDay(date: Date = new Date()): Post {
  const dayOfYear = getDayOfYear(date);
  const index = dayOfYear % DAILY_POSTS.length;
  return DAILY_POSTS[index]!;
}

function getDayOfYear(date: Date): number {
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  return Math.floor(diff / oneDay);
}

export function formatPost(post: Post): string {
  const tags = post.tags.map((t) => `#${t.replace(/\s+/g, "_")}`).join(" ");
  return `<b>${post.title}</b>\n\n${post.body}\n\n${tags}\n\n🔗 <a href="https://t.me/openclaw">OpenClaw</a> — цифровая безопасность`;
}

export function formatDangerAlert(url: string, count: number, threatTypes: string[], explanation: string): string {
  const threatList = threatTypes.length > 0
    ? threatTypes.map((t) => `• ${t}`).join("\n")
    : "• Подозрительная активность";

  return `🚨 <b>Внимание: опасная ссылка обнаружена ${count} раз</b>\n\n` +
    `<b>Домен:</b> <code>${url}</code>\n\n` +
    `<b>Угрозы:</b>\n${threatList}\n\n` +
    `<b>Что это значит:</b>\n${explanation}\n\n` +
    `❌ Не переходи по этой ссылке. Если получил её от знакомого — скажи ему об этом.\n\n` +
    `#опасно #фишинг #предупреждение\n\n` +
    `🔗 <a href="https://t.me/openclaw">OpenClaw</a> — проверяй ссылки перед открытием`;
}
