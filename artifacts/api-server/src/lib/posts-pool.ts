export type Rubric = "hygiene" | "breakdown" | "tool" | "quiz" | "story";

const POOL_MAP: Record<Rubric, Post[]> = {
  hygiene: HYGIENE_POSTS,
  breakdown: BREAKDOWN_POSTS,
  tool: TOOL_POSTS,
  quiz: QUIZ_POSTS,
  story: STORY_POSTS,
};

const RUBRIC_BY_WEEKDAY: Record<number, Rubric> = {
  0: "breakdown", // Воскресенье
  1: "hygiene",   // Понедельник
  2: "breakdown", // Вторник
  3: "tool",      // Среда
  4: "hygiene",   // Четверг
  5: "quiz",      // Пятница
  6: "story",     // Суббота
};

export function getRubricForDate(date: Date = new Date()): Rubric {
  return RUBRIC_BY_WEEKDAY[date.getDay()] ?? "hygiene";
}

export function getPostForDate(date: Date = new Date()): Post {
  const rubric = getRubricForDate(date);
  const pool = POOL_MAP[rubric];
  const dayOfYear = getDayOfYear(date);
  return pool[dayOfYear % pool.length]!;
}

function getDayOfYear(date: Date): number {
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - start.getTime();
  return Math.floor(diff / (1000 * 60 * 60 * 24));
}

export function formatPost(post: Post): string {
  const rubricEmoji: Record<Rubric, string> = {
    hygiene: "🧼",
    breakdown: "🎣",
    tool: "🛠",
    quiz: "🧪",
    story: "📖",
  };

  const rubricTag: Record<Rubric, string> = {
    hygiene: "#гигиена",
    breakdown: "#разбор",
    tool: "#инструмент",
    quiz: "#проверь_себя",
    story: "#история",
  };

  const emoji = rubricEmoji[post.rubric];
  const mainTag = rubricTag[post.rubric];
  const extraTags = post.tags.map((t) => `#${t.replace(/[\s-]/g, "_")}`).join(" ");

  return (
    `<b>${post.title}</b>\n\n` +
    `${post.body}\n\n` +
    `${mainTag} ${extraTags}\n\n` +
    `🔗 <a href="https://t.me/bezstrahavseti">OpenClaw</a> — цифровая безопасность`
  );
}

export function formatDangerAlert(
  url: string,
  count: number,
  threatTypes: string[],
  explanation: string
): string {
  const threatList =
    threatTypes.length > 0
      ? threatTypes.map((t) => `• ${t}`).join("\n")
      : "• Подозрительная активность";

  return (
    `🚨 <b>Внимание: опасная ссылка обнаружена ${count} раз</b>\n\n` +
    `<b>Домен:</b> <code>${url}</code>\n\n` +
    `<b>Угрозы:</b>\n${threatList}\n\n` +
    `<b>Что это значит:</b>\n${explanation}\n\n` +
    `❌ Не переходи по этой ссылке. Если получил её от знакомого — скажи ему об этом.\n\n` +
    `#опасно #фишинг #предупреждение\n\n` +
    `🔗 <a href="https://t.me/bezstrahavseti">OpenClaw</a> — проверяй ссылки перед открытием`
  );
}

